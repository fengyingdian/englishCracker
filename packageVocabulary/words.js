const getScore = right => {
  var wordsCount = 0
  if (right >= 0 && right<=5){
    wordsCount = 100*right
  } else if (right > 5 && right <= 10){
    wordsCount = 500 + 100 * right 
  } else if (right > 10 && right <= 20) {
    wordsCount = 1500 + 100 * right
  } else if (right > 20 && right <= 30) {
    wordsCount = 3500 + 150 * right
  } else if (right > 30 && right <= 35) {
    wordsCount = 8000 + 100 * right
  } else if (right > 35 && right <= 40) {
    wordsCount = 13500 + 100 * right
  }
  var random = Math.floor(Math.random() * 10)*10 + Math.floor(Math.random() * 10)
  console.log("count: ", wordsCount, random)
  wordsCount = wordsCount + random
  return wordsCount
}

